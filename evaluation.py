def evaluate_material(board):
    score = 0
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece:
            value = PIECE_VALUES.get(piece.piece_type, 0)
            score += value if piece.color == chess.WHITE else -value
    return score


def evaluate_position(board):
    score = 0
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece:
            piece_symbol = piece.symbol()
            color = "w" if piece.color == chess.WHITE else "b"
            key = color + piece_symbol.upper()
            position_scores = piece_position_scores.get(key, [[0] * 8] * 8)

            row, col = divmod(square, 8)
            score += position_scores[row][col] if piece.color == chess.WHITE else -position_scores[row][col]
    return score

def value_state(board):
    if board.is_checkmate():
        return CHECKMATE if board.turn else -CHECKMATE

    if board.is_stalemate() or board.is_insufficient_material():
        return 0
    material_score = evaluate_material(board)
    positional_score = evaluate_position(board)

    total_score = material_score + positional_score
    if board.turn:
        total_score *= -1

    return total_score

